    private void jButtonNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNewActionPerformed

        String name = JOptionPane.showInputDialog(this,
                java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_NEW_MSG"),
                java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_NEW_TITLE"),
                JOptionPane.QUESTION_MESSAGE);

        if (name != null && !name.trim().isEmpty()) {
            PlayerConfig newConfig = new PlayerConfigSimpleImpl(name);

            this.configModel.addElement(newConfig);
            this.configModel.setSelectedItem(newConfig);
        }
    }//GEN-LAST:event_jButtonNewActionPerformed

    private void jButtonRenameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRenameActionPerformed

        if (this.currentConfig != null) {
            String name = (String) JOptionPane.showInputDialog(this,
                    java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_RENAME_MSG"),
                    java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_RENAME_TITLE"),
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    null,
                    this.currentConfig.getName());

            if (name != null && !name.trim().isEmpty()) {
                this.currentConfig.setName(name);
                this.configModel.setSelectedItem(this.currentConfig);
            }
        }
    }//GEN-LAST:event_jButtonRenameActionPerformed

    private void jComboBoxConfigsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxConfigsActionPerformed
        this.currentConfig = (PlayerConfig) this.configModel.getSelectedItem();
        this.refreshConfigPanel();
    }//GEN-LAST:event_jComboBoxConfigsActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        if (this.currentConfig != null) {

            if (JOptionPane.showConfirmDialog(
                    this,
                    java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_DELETE_MSG"),
                    java.util.ResourceBundle.getBundle("lv/llu/rembot/ctr/language").getString("PLAYER_CONFIG_DELETE_TITLE"),
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION) {
                this.configModel.removeElement(this.currentConfig);
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

