    private void refreshConfigPanel() {
        Boolean isValid = this.currentConfig != null;
        if (isValid) {
            this.jTextFieldDescr.setText(this.currentConfig.getDescription());
            this.jTextAreaConfig.setText(this.currentConfig.getConfigText());
        }

        this.jPanelConfigDetails.setVisible(isValid);
        this.jButtonRename.setEnabled(isValid);
        this.jButtonDelete.setEnabled(isValid);
        this.jButtonStart.setEnabled(isValid);

    }

